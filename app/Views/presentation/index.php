            <p>FACE Territoire Bourbonnais est née en Juillet 2015.

Il s'agit d'un club d'entrepreneurs sous forme associative en accord avec les collectivités locales, regroupant des entreprises qui souhaitent participer au développement économique et social du territoire. FACE TERRITOIRE BOURBONNAIS doit être un facilitateur, un révélateur de talents pour arriver à la réussite professionnelle.</p>

<p>
Il est une déclinaison locale de FACE, Fondation Agir Contre l'Exclusion, fondation de grands groupes nationaux, ayant pour vocation de favoriser l'engagement social et sociétal des entreprises pour lutter contre toutes formes d'exclusion, de discrimination et de pauvreté, par une approche globale et innovante de la RSE ou Responsabilité Sociale d'Entreprise. </p>

 
<p>
En octobre 2016, FACE TERRITOIRE BOURBONNAIS a répondu à un appel à projet sur la création de formations aux métiers du numérique, dans le cadre d'une opération nationale baptisée "la Grande Ecole Du Numérique".  </p>

 
<p>
Notre club a été retenu pour proposer d'ici la fin 2016 une formation courte (8 mois) et non diplômante au métier de développeur WEB à l'attention de 23 bénéficiaires, demandeurs d'emploi et/ou éloignés des parcours scolaires traditionnels.</p>

<p>
Le Club FACE TERRITOIRE BOURBONNAIS est aussi un lieu de réflexion et d'échanges entre dirigeants d'entreprises
</p>
<p>
Les grandes actions du Club :

- création de formations courtes et spécifiques aux besoins des entreprises (appel à projet de la Grande Ecole du Numérique) - détection de talents locaux ;

- créer et développer les CV Vidéos et Offres vidéos (CV à l'envers)

- Les Cafés d'En Face pour échanger entre les jeunes et chefs d'entreprises ;

- contribuer à l'insertion professionnelle et sociale des demandeurs d'emploi, jeunes et séniors en favorisant le parrainage ;

- concevoir, expérimenter, développer et accompagner les entreprises dans le développement des pratiques RSE
</p>



